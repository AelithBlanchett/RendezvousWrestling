    where TModifier : BaseModifier<TAchievement, TActionFactory, TActiveAction, TFeature, TFeatureFactory, TFight, TFighterState, TModifier, TUser, OptionalParameterType>
{
    public TFight fight { get; set; }
    public TFighterState fighter { get; set; }
    public TFighterState target { get; set; }
    public TActiveAction action { get; set; }

    public BaseFeatureParameter(TFight fight = null, TFighterState fighter = null, TFighterState target = null, TActiveAction action = null)
